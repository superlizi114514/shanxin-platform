def preprocess_text(text):
    """
    预处理：合并行，使每个课程的完整信息在一行上

    观察：
    - 课程详情通常以 (X-Y 节) 开头
    - 下一个课程详情开始前，当前课程的所有信息应该已经完整

    策略：
    1. 找到所有以 (X-Y 节) 开头的片段
    2. 合并直到下一个 (X-Y 节) 之前的所有内容
    """
    # 首先，将所有行用空格连接成一个长字符串
    full_text = ' '.join(line.strip() for line in text.split('\n') if line.strip())

    # 然后，在每个 (X-Y 节) 前面插入换行符，以便分割
    # 这样可以分离每个课程的详情
    separated = re.sub(r'\((\d+)-(\d+)\s* 节 \)', r'\n(\1-\2 节)', full_text)

    return separated


def parse_format5_v5(text):
    """
    V5 解析器 - 基于预处理后的文本
    """
    courses = []

    # 预处理：分离每个课程块
    separated = preprocess_text(text)
    blocks = separated.split('\n')

    print(f"预处理后得到 {len(blocks)} 个块")

    # 课程名模式：中文字符 + 可选的英文/数字 + 课程符号
    course_name_pattern = re.compile(r'([^\s☆★◎●※]+[☆★◎●※])')

    # 课程详情模式：(节次) 周次/教室/教师
    detail_pattern = re.compile(r'\((\d+)-(\d+)\s* 节 \)([^/]+)/([^/]+)/([^/\n]+)')

    for block in blocks:
        if not block.strip():
            continue

        # 提取课程名
        name_matches = list(course_name_pattern.finditer(block))
        if not name_matches:
            continue

        # 提取详情
        detail_match = detail_pattern.search(block)
        if not detail_match:
            continue

        # 可能有多个课程名，但只有一个详情块
        # 我们需要确定哪个课程名对应这个详情
        # 简单策略：取详情前面的最后一个课程名

        detail_pos = detail_match.start()
        relevant_name = None
        for name_match in name_matches:
            if name_match.end() <= detail_pos:
                relevant_name = name_match.group(1)
            else:
                break

        if not relevant_name:
            # 如果没有找到在详情前的课程名，取第一个
            relevant_name = name_matches[0].group(1)

        # 清理课程名（移除符号）
        course_name = re.sub(r'[☆★◎●※]', '', relevant_name).strip()

        # 提取详情字段
        start_period = detail_match.group(1)
        end_period = detail_match.group(2)
        week_range = detail_match.group(3).strip()
        classroom_raw = detail_match.group(4).strip()
        teacher_raw = detail_match.group(5).strip()

        # 清理教室名
        classroom = re.sub(r'(教室 | 合堂 | 公共 | 中心 | 微机室 | 网络空间 | 培养中心 | 考试认证 | 与)', '', classroom_raw)

        # 提取教师名
        teacher_match = re.match(r'^([\u4e00-\u9fa5]{2,6}|[\u4e00-\u9fa5]+ 外教)', teacher_raw)
        teacher = teacher_match.group(1) if teacher_match else ''

        # 验证：课程名不能太短，必须有教师
        if len(course_name) < 2 or not teacher:
            print(f"  [SKIP] {course_name}: 课程名太短或无教师")
            continue

        # 确定星期几（基于块在原文本中的位置）
        day_of_week = estimate_day_of_week(text, block)

        print(f"  [OK] {course_name}: ({start_period}-{end_period}节) | {week_range} | {classroom} | {teacher} | 周{day_of_week}")

        courses.append({
            'course_name': course_name,
            'period': f"{start_period}-{end_period}节",
            'week_range': week_range,
            'classroom': classroom,
            'teacher': teacher,
            'day_of_week': day_of_week
        })

    return courses


def estimate_day_of_week(original_text, block):
    """
    根据课程块在原文本中的位置，估算它属于星期几

    策略：
    1. 找到星期标题的位置
    2. 找到课程块的位置
    3. 判断课程块位于哪个星期标题的"列"中
    """
    # 星期标题
    days = ['星期一', '星期二', '星期三', '星期四', '星期五', '星期六', '星期日']
    day_map = {'星期一': 1, '星期二': 2, '星期三': 3, '星期四': 4, '星期五': 5, '星期六': 6, '星期日': 7}

    # 找到所有星期标题的位置
    day_positions = []
    for day in days:
        pos = original_text.find(day)
        if pos != -1:
            day_positions.append((day, pos, day_map[day]))

    if not day_positions:
        return 1  # 默认星期一

    # 找到课程块在原文本中的位置
    # 使用块的第一个非空白字符作为位置
    block_stripped = block.strip()[:20]  # 取前 20 个字符作为标识
    block_pos = original_text.find(block_stripped)

    if block_pos == -1:
        # 如果找不到，使用原文本中块的第一次出现
        return 1

    # 找到课程位置之前的最近星期标题
    closest_day = 1
    closest_pos = -1
    for day, pos, day_num in day_positions:
        if pos < block_pos and pos > closest_pos:
            closest_pos = pos
            closest_day = day_num

    return closest_day


