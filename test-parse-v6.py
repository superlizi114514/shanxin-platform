def parse_format5_v6(text):
    """
    V6 解析器 - 使用正确的正则模式

    策略：
    1. 将整个文本合并成一行
    2. 使用 \s* 匹配任意空白（包括中文空格）
    3. 直接匹配完整模式：课程名☆ (节次) 周次/教室/教师
    """
    courses = []

    # 合并所有行
    full_text = ' '.join(line.strip() for line in text.split('\n') if line.strip())
    print(f"合并后文本长度：{len(full_text)}")

    # 使用更宽松的正则
    # 模式：(X-Y 节) 周次/教室/教师
    # 使用 \s* 匹配任意空白
    detail_pattern = re.compile(r'\((\d+)-(\d+)\s* 节 \s*([^\n/]+)/([^\n/]+)/([^\n/]+)')

    matches = list(detail_pattern.finditer(full_text))
    print(f"匹配到 {len(matches)} 个课程详情")

    for match in matches:
        start_period = match.group(1)
        end_period = match.group(2)
        week_range = match.group(3).strip()
        classroom_raw = match.group(4).strip()
        teacher_raw = match.group(5).strip()

        # 清理教室
        classroom = re.sub(r'(教室 | 合堂 | 公共 | 中心 | 微机室 | 网络空间 | 培养中心 | 考试认证 | 与)', '', classroom_raw)

        # 提取教师
        teacher_match = re.match(r'^([\u4e00-\u9fa5]{2,6}|[\u4e00-\u9fa5]+ 外教)', teacher_raw)
        teacher = teacher_match.group(1) if teacher_match else ''

        if not teacher:
            continue

        # 找到这个详情前面的课程名
        pos = match.start()
        before = full_text[:pos]

        # 找到最近的☆符号前的课程名
        symbol_match = None
        for m in re.finditer(r'([^\s☆★◎●※]+)([☆★◎●※])', before):
            symbol_match = m

        course_name = ''
        if symbol_match:
            course_name = re.sub(r'[☆★◎●※]', '', symbol_match.group(1)).strip()

        if len(course_name) < 2:
            continue

        print(f"  [OK] {course_name}: ({start_period}-{end_period}节) | {classroom} | {teacher}")

        courses.append({
            'course_name': course_name,
            'period': f"{start_period}-{end_period}节",
            'week_range': week_range,
            'classroom': classroom,
            'teacher': teacher
        })

    return courses


