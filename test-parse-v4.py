def parse_format5_v4(text):
    """
    V4 版本 - 基于课程符号定位，然后提取详情

    核心思路：
    1. 找到所有带☆★◎●※的课程名
    2. 课程名后面应该跟着 (X-Y 节) 格式的节次信息
    3. 然后提取 /教室/教师 信息
    """
    courses = []

    # 课程类型符号
    SYMBOLS = '☆★◎●※'

    # 按行分割
    lines = text.split('\n')

    # 合并所有文本为单行（用空格连接），方便跨行匹配
    full_text = ' '.join(line.strip() for line in lines if line.strip())

    print(f"合并后文本长度：{len(full_text)}")
    print(f"文本预览：{full_text[:500]}...")

    # 找到所有课程符号的位置
    symbol_positions = []
    for i, char in enumerate(full_text):
        if char in SYMBOLS:
            # 提取符号前的课程名
            start = max(0, i - 50)  # 向前最多 50 字符
            before = full_text[start:i]
            # 课程名应该是最后一个空白字符到符号之间
            name_match = re.search(r'([^\s]+)$', before)
            if name_match:
                course_name = name_match.group(1)
                symbol_positions.append({
                    'name': course_name,
                    'symbol': char,
                    'pos': i
                })
                print(f"找到课程：{course_name} ({char}) @ {i}")

    print(f"\n共找到 {len(symbol_positions)} 个课程符号")

    # 对每个课程，尝试提取详情
    for course in symbol_positions:
        pos = course['pos']
        # 查找符号后面的 (X-Y 节) 模式
        after = full_text[pos+1:pos+200]  # 向后看 200 字符

        # 匹配节次：(1-2 节) 或 (1-4 节) 等
        period_match = re.search(r'\((\d+)-(\d+)\s* 节 \)', after)
        if not period_match:
            print(f"  [SKIP] {course['name']}: 未找到节次信息")
            continue

        start_period = period_match.group(1)
        end_period = period_match.group(2)
        period_end = period_match.end()

        # 节次后面应该是周次/教室/教师
        detail_text = after[period_end:period_end+300]

        # 提取周次（到第一个/之前）
        slash_pos = detail_text.find('/')
        if slash_pos == -1:
            print(f"  [SKIP] {course['name']}: 未找到/分隔符")
            continue

        week_range = detail_text[:slash_pos].strip()

        # 提取教室和教师
        remaining = detail_text[slash_pos+1:]

        # 教室通常是 K 开头 + 数字，或者包含"教室"、"合堂"等
        # 教师是 2-6 个中文字符

        # 先找教室
        classroom_match = re.match(r'([^\s/]+?(?:教室 | 合堂 | 微机室 | 中心 | 室 | 篮球场|场地)?[^\s/]*)', remaining)
        if not classroom_match:
            print(f"  [SKIP] {course['name']}: 未找到教室")
            continue

        classroom = classroom_match.group(1)

        # 清理教室名
        classroom_clean = re.sub(r'(教室 | 合堂 | 公共 | 中心 | 微机室 | 网络空间 | 培养中心 | 考试认证 | 与)', '', classroom)

        # 找教师（在下一个/后面）
        after_classroom = remaining[classroom_match.end():]
        teacher_slash = after_classroom.find('/')
        if teacher_slash == -1:
            print(f"  [SKIP] {course['name']}: 未找到教师分隔符")
            continue

        teacher_raw = after_classroom[:teacher_slash].strip()

        # 提取教师名（2-6 个中文字符，或包含"外教"）
        teacher_match = re.match(r'^([\u4e00-\u9fa5]{2,6}|[\u4e00-\u9fa5]+ 外教)', teacher_raw)
        teacher = teacher_match.group(1) if teacher_match else ''

        if not teacher:
            print(f"  [SKIP] {course['name']}: 未找到有效教师 (原始：{teacher_raw[:20]})")
            continue

        print(f"  [OK] {course['name']}: ({start_period}-{end_period}节) | {week_range} | {classroom_clean} | {teacher}")

        courses.append({
            'course_name': course['name'],
            'symbol': course['symbol'],
            'period': f"{start_period}-{end_period}节",
            'week_range': week_range,
            'classroom': classroom_clean,
            'teacher': teacher
        })

    return courses

